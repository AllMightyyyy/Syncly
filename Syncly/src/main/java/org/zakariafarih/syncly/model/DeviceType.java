package org.zakariafarih.syncly.model;

/**
 * Enum representing different types of devices.
 * Used to categorize devices in the system.
 */
public enum DeviceType {
    /**
     * Represents a desktop device.
     */
    DESKTOP,

    /**
     * Represents a mobile device.
     */
    MOBILE,

    /**
     * Represents a web-based device.
     */
    WEB,

    /**
     * Represents any other type of device not covered by the other categories.
     */
    OTHER
}